// Large Shirts:
// Modify the make_shirt() function so that shirts are large by default with a message that reads I love TypeScript. 
// Make a large shirt and a medium shirt with the default message, and a shirt of any size with a different message.
function make_shirtL(text) {
    console.log("your shirt size is L and your printed text will be \"".concat(text, "\""));
}
make_shirtL('I love TypeScript');
function make_shirtM(text) {
    console.log("your shirt size is M and your printed text will be \"".concat(text, "\""));
}
make_shirtM('I love TypeScript');
function make_shirtXXL(text) {
    console.log("your shirt size is XXL and your printed text will be \"".concat(text, "\""));
}
make_shirtXXL('I love TypeScript');
